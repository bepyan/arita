export const GET: APIRoute = async () => {
  const [dotumBold, dotumThin] = await Promise.all([
    readFile('fonts/AritaDotumKR-Bold.ttf'),
    readFile('fonts/AritaDotumKR-Thin.ttf'),
  ]);

  const svg = await satori(markup, {
    width: WIDTH,
    height: HEIGHT,
    fonts: [
      { name: 'Arita Dotum', data: dotumBold, weight: 700, style: 'normal' },
      { name: 'Arita Dotum', data: dotumThin, weight: 100, style: 'normal' },
    ],
  });

  const png = new Resvg(svg, {
    fitTo: { mode: 'width', value: WIDTH * SCALE },
  })
    .render()
    .asPng();

  return new Response(new Uint8Array(png), {
    headers: { 'Content-Type': 'image/png' },
  });
};
